public class DotThi {
    String idDotThi;
    String tenDotThi;
    public DotThi(){

    }
    public DotThi(String idDotThi, String tenDotThi) {
        this.idDotThi = idDotThi;
        this.tenDotThi = tenDotThi;
    }

    public String getIdDotThi() {
        return idDotThi;
    }

    public String getTenDotThi() {
        return tenDotThi;
    }
}
